export default function MobileBottomNav() {
  const { user } = useAuth()

  const links = [
    { to: '/', icon: HomeIcon, label: 'HOME', end: true },
    { to: '/products', icon: ProductsIcon, label: 'PRODUCTS', end: false },
    { to: '/combos', icon: CombosIcon, label: 'COMBOS', end: false },
    { to: '/about', icon: AboutIcon, label: 'ABOUT', end: false },
    { to: user ? '/account' : '/login', icon: AccountIcon, label: 'ACCOUNT', end: false },
  ]

  return (
    <nav className="fixed bottom-0 left-0 right-0 z-50 border-t border-slate-200 bg-white/95 backdrop-blur-md md:hidden safe-area-bottom">
      <div className="flex items-center justify-around py-1">
        {links.map(link => (
          <NavLink
            key={link.to}
            to={link.to}
            end={link.end}
            className={({ isActive }) =>
              `flex flex-col items-center gap-0.5 px-3 py-1 text-[10px] font-semibold transition ${
                isActive ? 'text-brand-600' : 'text-slate-500 hover:text-slate-700'
              }`
            }
          >
            <link.icon className="h-5 w-5" />
            {link.label}
          </NavLink>
        ))}
      </div>
    </nav>
  )
}
